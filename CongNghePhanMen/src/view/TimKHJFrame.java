    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        KhachHangDAO khDao = new KhachHangDAO();
        try {
            list = khDao.searchKhachHang(jtfTenKH.getText().trim());
        } catch (SQLException ex) {

        }
        String[] column = {"Mã Khách Hàng", "Tên Khách Hàng", "Số điện thoại", "Địa Chỉ"};
        String[][] value = new String[list.size()][5];
        for (int i = 0; i < list.size(); i++) {
            value[i][0] = list.get(i).getMaKH() + "";
            value[i][1] = list.get(i).getTenKH() + "";
            value[i][2] = list.get(i).getSdt() + "";
            value[i][3] = list.get(i).getDiaChi() + "";
        }
        DefaultTableModel model = new DefaultTableModel(value, column) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jtlTable.setModel(model);
    }//GEN-LAST:event_btnSearchActionPerformed

