    public TimKHJFrame(SanBong sanBong, BookingTime bookingTime) {
        san.setMaSan(sanBong.getMaSan());
        san.setLoaiSan(sanBong.getLoaiSan());
        san.setKhungGio(sanBong.getKhungGio());
        san.setGia(sanBong.getGia());
        time.setNgayBatdau(bookingTime.getNgayBatdau());
        time.setNgayKetthuc(bookingTime.getNgayKetthuc());
        initComponents();
        list = new ArrayList<KhachHang>();
        jtlTable.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2 && jtlTable.getSelectedRow() != -1) {
                    try {
                        DefaultTableModel model = (DefaultTableModel) jtlTable.getModel();
                        int selectRowIndex = jtlTable.getSelectedRow();
                        selectRowIndex = jtlTable.convertRowIndexToModel(selectRowIndex);
                        KhachHang khachHang = new KhachHang();
                        khachHang.setMaKH(jtlTable.getValueAt(selectRowIndex, 0).toString());
                        khachHang.setTenKH(jtlTable.getValueAt(selectRowIndex, 1).toString());
                        khachHang.setSdt(jtlTable.getValueAt(selectRowIndex, 2).toString());
                        khachHang.setDiaChi(jtlTable.getValueAt(selectRowIndex, 3).toString());
//                    System.out.println(sanBong.getMaSan() + " " + sanBong.getLoaiSan() + " " + sanBong.getKhungGio() + " " + sanBong.getGia());
//                    System.out.println(bookingTime.getNgayBatdau() + "++" + bookingTime.getNgayKetthuc());

                        JFrame jframe = new DatSanJFrame(khachHang, sanBong, bookingTime);
                        jframe.setVisible(true);
                        jframe.setTitle("Chi tiết phiếu đặt sân");
                        dispose();
                    } catch (SQLException ex) {
                        Logger.getLogger(TimKHJFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }

        });
    }

